            else
            {
                this.Text = "Institución - Alta";
            }
        }

        // Valida los datos antes de realizar cualquier operación
        private void Validar()
        {
            if (!txbCuit.Text.Equals("  -        -"))
            {
                if (!txbCuit.MaskFull)
                {
                    txbCuit.Focus();
                    throw new ArgumentException("El CUIT ingresado es incompleto.");
                }
                if (!CuitValido())
                {
                    txbCuit.Focus();
                    throw new ArgumentException("Comprobación del dígito verificador incorrecta. El CUIT ingresado no es válido.");
                }
            }

            if (txbRazonSocial.Text.Trim() == "")
            {
                txbRazonSocial.Focus();
                throw new ArgumentException("Debe ingresar la razón social de la institución.");
            }

            if (txbLocalidades.Text != "")
            {
                if (!source.Contains(txbLocalidades.Text))
                {
                    txbLocalidades.Focus();
                    throw new ArgumentException("La localidad ingresada no es correcta.");
                }
            }

            if(!EmailValido() && !txbEmail.Text.Equals(""))
            {
                txbEmail.Focus();
                throw new ArgumentException("El formato de Email ingresado es inválido.");
            }
        }

        private bool EmailValido()
        {
            String expresion;
            expresion = "\\w+([-+.']\\w+)*@\\w+([-.]\\w+)*\\.\\w+([-.]\\w+)*";
            if (Regex.IsMatch(txbEmail.Text, expresion))
            {
                if (Regex.Replace(txbEmail.Text, expresion, String.Empty).Length == 0)
                {
                    return true;
                }
                else
                {
                    return false;
                }
            }
            else
            {
                return false;
            }
        }

        private bool CuitValido()
        {
            int[] mult = new[] { 5, 4, 3, 2, 7, 6, 5, 4, 3, 2 };
            char[] nums;
            int total = 0;
            int resultado;

            string a = txbCuit.Text.Replace("-", "");

            if (a.Count() == 11)
            {
                int digVer = Convert.ToInt32(a.Substring(10, 1));

                a = a.Remove(10);

                nums = a.ToCharArray();

                for (int i = 0; i < mult.Length; i++)
                {
                    total += int.Parse(nums[i].ToString()) * mult[i];
                }

                var resto = total % 11;

                switch (resto)
                {
                    case 0: resultado = 0; break;
                    case 1: resultado = 9; break;
                    default: resultado = (11 - resto); break;
                }

                if (resultado == digVer)
                {
                    return true;
                }
                else
                {
                    return false;
                }
            }
            else
            {
                return false;
            }
        }

        // Confirmar la operación en curso
        private void AceptarOperacion()
        {
            SistemaARA.Negocio.Instituciones oInstitucionesNegocio = new SistemaARA.Negocio.Instituciones();
            SistemaARA.Entidades.Institucion oInstitucion = new SistemaARA.Entidades.Institucion();
            SistemaARA.Entidades.Instituciones oInstituciones;

            try
            {
                //Valido los datos del formulario
                Validar();

                // Si todo está bien creo un objeto contacto con la nueva informacion
                oInstitucion.cuit = txbCuit.Text.Trim();
                oInstitucion.razonSocial = CultureInfo.InvariantCulture.TextInfo.ToTitleCase(txbRazonSocial.Text.Trim().ToLower());
                oInstitucion.direccion = CultureInfo.InvariantCulture.TextInfo.ToTitleCase(txbDireccion.Text.Trim().ToLower());
                oInstitucion.nombreContacto = CultureInfo.InvariantCulture.TextInfo.ToTitleCase(txbNombreContacto.Text.Trim().ToLower());
                oInstitucion.telefono = txbTelefono.Text.Trim();
                oInstitucion.telCelular = txbTelCelular.Text.Trim();
                oInstitucion.email = txbEmail.Text.Trim();
                oInstitucion.localidad = txbLocalidades.Text.Trim();

                // Verifico operación
                if (Operacion == General.TipoOperacion.Alta)
                {
                    if (!txbCuit.Text.Equals("  -        -"))
                    {
                        oInstituciones = oInstitucionesNegocio.GetOne(txbCuit.Text);

                        if (oInstituciones.Count > 0)
                        {
                            if (oInstituciones[0].baja == 0)
                            {
                                txbCuit.Focus();
                                throw new ArgumentException("Ya existe una institución con el CUIT ingresado.");
                            }
                            else
                            {
                                oInstitucion.baja = 0;

                                oInstitucion.idInstitucion = oInstituciones[0].idInstitucion;
                                oInstitucionesNegocio.Update(oInstitucion);
                                MessageBox.Show("Institución restaurada exitosamente.", this.Text, MessageBoxButtons.OK, MessageBoxIcon.Information);
                            }
                        }
                        else
                        {
                            // Agrego nueva institución
                            oInstitucionesNegocio.Add(oInstitucion);
                            MessageBox.Show("Institución agregada exitosamente.", this.Text, MessageBoxButtons.OK, MessageBoxIcon.Information);
                        }
                    }
                    else
                    {
                        oInstituciones = oInstitucionesNegocio.GetOneRS(txbRazonSocial.Text);

                        if (oInstituciones.Count > 0)
                        {
                            if (oInstituciones[0].baja == 0)
                            {
                                txbCuit.Focus();
                                throw new ArgumentException("Ya existe una institución con la razón social ingresada.");
                            }
                            else
                            {
                                oInstitucion.baja = 0;

                                oInstitucion.idInstitucion = oInstituciones[0].idInstitucion;
                                oInstitucionesNegocio.Update(oInstitucion);
                                MessageBox.Show("Institución restaurada exitosamente.", this.Text, MessageBoxButtons.OK, MessageBoxIcon.Information);
                            }
                        }
                    }
                }
                else
                {
                    if (!txbCuit.Text.Equals("  -        -"))
                    {
                        oInstituciones = oInstitucionesNegocio.GetOne(txbCuit.Text);
                    }
                    else
                    {
                        oInstituciones = oInstitucionesNegocio.GetOneRS(txbRazonSocial.Text);
                    }

                    if (oInstituciones.Count > 0)
                    {
                        if (oInstituciones[0].idInstitucion == IdInstitucion)
                        {
                            // Actualizo institución existente
                            oInstitucion.idInstitucion = IdInstitucion;
                            oInstitucionesNegocio.Update(oInstitucion);
                            MessageBox.Show("Institución actualizada exitosamente.", this.Text, MessageBoxButtons.OK, MessageBoxIcon.Information);
                        }
                        else
                        {
                            if (!txbCuit.Text.Equals("  -        -"))
                            {
                                txbCuit.Focus();
                                throw new ArgumentException("Ya existe una institución con el CUIT ingresado.");
                            }
                            else
                            {
                                txbRazonSocial.Focus();
                                throw new ArgumentException("Ya existe una institución con la razón social ingresada.");
                            }
                        }
                    }
                    else
                    {
                        // Actualizo institución existente
                        oInstitucion.idInstitucion = IdInstitucion;
                        oInstitucionesNegocio.Update(oInstitucion);
                        MessageBox.Show("Institución actualizada exitosamente.", this.Text, MessageBoxButtons.OK, MessageBoxIcon.Information);
                    }
                }

                // Indico que todo salio bien y cierro el formulario
                Cancelado = false;
                this.Hide();
            }
            catch (Exception ex)
            {
                // Muestro cualquier error de la aplicacion o por validación
                MessageBox.Show(ex.Message, this.Text, MessageBoxButtons.OK, MessageBoxIcon.Information);
            }
            finally
            {
                // Libera objetos
                oInstitucion = null;
                oInstituciones = null;
                oInstitucionesNegocio = null;
            }
        }

