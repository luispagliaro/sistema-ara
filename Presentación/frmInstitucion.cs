                        txbNombreContacto.Text = oInstitucion.nombreContacto;
                        txbTelefono.Text = oInstitucion.telefono;
                        txbTelCelular.Text = oInstitucion.telCelular;
                        txbEmail.Text = oInstitucion.email;
                        txbLocalidades.Text = oInstitucion.localidad;
